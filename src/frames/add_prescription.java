    private void patient_name_txtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_patient_name_txtKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getKeyCode() == KeyEvent.VK_DELETE || patient_name_txt.getText().equals("")) {
        } else {
            String to_check = patient_name_txt.getText();
            int to_check_len = to_check.length();
            for (String data : s) {

                String check_from_data = "";
                for (int i = 0; i < to_check_len; i++) {
                    if (to_check_len <= data.length()) {
                        check_from_data = check_from_data + data.charAt(i);
                    }
                }

                if (check_from_data.equals(patient_name_txt.getText())) {

                    patient_name_txt.setText(data);
                    patient_name_txt.setSelectionStart(to_check_len);
                    patient_name_txt.setSelectionEnd(data.length());

                }

            }
        }
    }//GEN-LAST:event_patient_name_txtKeyReleased

