    private void data_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_data_tableMouseClicked

        try {
            edit_date_chooser.setDate(myFormat.parse(data_table.getModel().getValueAt(data_table.getSelectedRow(), 2).toString()));
            edit_quantity_txt.setText(data_table.getModel().getValueAt(data_table.getSelectedRow(), 1).toString());
            edit_entered_name_txt.setText(data_table.getModel().getValueAt(data_table.getSelectedRow(), 0).toString());
            edit_patient_name_txt.setText(data_table.getModel().getValueAt(data_table.getSelectedRow(), 4).toString());
            edit_job_txt.setText(data_table.getModel().getValueAt(data_table.getSelectedRow(), 3).toString());
            edit_degree_combo.setSelectedItem(data_table.getModel().getValueAt(data_table.getSelectedRow(), 5).toString());
            id = data_table.getModel().getValueAt(data_table.getSelectedRow(), 6).toString();
            String sql = "select image from data where id='" + id + "'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.first()) {
                person_image = rs.getBytes("image");
                formate = new ImageIcon(person_image);
                edit_image_label.setIcon(formate);
            }
            data_dialog.setSize(1070, 775);
            data_dialog.setLocationRelativeTo(null);
            data_dialog.setVisible(true);
            data_dialog.addWindowListener(new java.awt.event.WindowAdapter() {
                @Override
                public void windowClosing(java.awt.event.WindowEvent windowEvent) {
                    data_dialog.dispose();
                }
            });
        } catch (Exception ex) {
            Logger.getLogger(reports.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_data_tableMouseClicked

