    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed
        try {
            if (!edit_quantity_txt.getText().equals("") && !edit_entered_name_txt.getText().equals("") && !edit_patient_name_txt.getText().equals("") && !edit_job_txt.getText().equals("")) {

                String sql = "update data set date=?,quantity=?,entered_name=?,image=?,patient_name=?,degree=?,job=? where id='" + id + "'";
                pst = con.prepareStatement(sql);
                pst.setString(1, myFormat.format(edit_date_chooser.getDate()));
                pst.setString(2, edit_quantity_txt.getText());
                pst.setString(3, edit_entered_name_txt.getText());
                pst.setBytes(4, person_image);
                pst.setString(5, edit_patient_name_txt.getText());
                pst.setString(6, edit_degree_combo.getSelectedItem().toString());
                pst.setString(7, edit_job_txt.getText());
                pst.execute();
                get_data();
                JOptionPane.showMessageDialog(null, "تم التحديث بنجاح");
            } else {
                JOptionPane.showMessageDialog(null, "من فضلك املأ جميع البيانات!");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_update_btnActionPerformed

    private void edit_attachimage_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_attachimage_btnActionPerformed

        try {
            JFileChooser chooser = new JFileChooser();
            int result = chooser.showOpenDialog(null);

            if (result == JFileChooser.APPROVE_OPTION) {

                String filename = chooser.getSelectedFile().getAbsolutePath();

                File input = new File(filename);
                BufferedImage image = ImageIO.read(input);

                BufferedImage resized = resize(image, 500, 400);

                File output = new File("src\\images\\" + input.getName());
                ImageIO.write(resized, "jpeg", output);

                FileInputStream fis = new FileInputStream(output);
                ByteArrayOutputStream baos = new ByteArrayOutputStream();

                byte[] buf = new byte[1024];
                for (int readNum; (readNum = fis.read(buf)) != -1;) {
                    baos.write(buf, 0, readNum);
                }

                person_image = baos.toByteArray();
                ImageIcon formate = new ImageIcon(person_image);
                edit_image_label.setIcon(formate);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_edit_attachimage_btnActionPerformed

    private void edit_patient_name_txtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edit_patient_name_txtKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getKeyCode() == KeyEvent.VK_DELETE || edit_patient_name_txt.getText().equals("")) {
        } else {
            String to_check = edit_patient_name_txt.getText();
            int to_check_len = to_check.length();
            for (String data : s) {

                String check_from_data = "";
                for (int i = 0; i < to_check_len; i++) {
                    if (to_check_len <= data.length()) {
                        check_from_data = check_from_data + data.charAt(i);
                    }
                }

                if (check_from_data.equals(edit_patient_name_txt.getText())) {

                    edit_patient_name_txt.setText(data);
                    edit_patient_name_txt.setSelectionStart(to_check_len);
                    edit_patient_name_txt.setSelectionEnd(data.length());

                }

            }
        }
    }//GEN-LAST:event_edit_patient_name_txtKeyReleased

