    private void attachimage_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_attachimage_btnActionPerformed

        try {
            JFileChooser chooser = new JFileChooser();
            int result = chooser.showOpenDialog(null);

            if (result == JFileChooser.APPROVE_OPTION) {

                String filename = chooser.getSelectedFile().getAbsolutePath();

                File input = new File(filename);
                BufferedImage image = ImageIO.read(input);

                BufferedImage resized = resize(image, 500, 400);

                File output = new File("src\\images\\" + input.getName());
                ImageIO.write(resized, "jpeg", output);

                FileInputStream fis = new FileInputStream(output);

                ByteArrayOutputStream baos = new ByteArrayOutputStream();

                byte[] buf = new byte[1024];
                for (int readNum; (readNum = fis.read(buf)) != -1;) {
                    baos.write(buf, 0, readNum);
                }

                person_image = baos.toByteArray();
                ImageIcon formate = new ImageIcon(person_image);
                image_label.setIcon(formate);

            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_attachimage_btnActionPerformed

    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        try {
            if (person_image != null && !quantity_txt.getText().equals("") && !entered_name_txt.getText().equals("") && !patient_name_txt.getText().equals("") && !job_txt.getText().equals("")) {

                String sql = "insert into data (date,quantity,entered_name,image,patient_name,degree,job) values(?,?,?,?,?,?,?)";
                pst = con.prepareStatement(sql);
                pst.setString(1, myFormat.format(date_chooser.getDate()));
                pst.setString(2, quantity_txt.getText());
                pst.setString(3, entered_name_txt.getText());
                pst.setBytes(4, person_image);
                pst.setString(5, patient_name_txt.getText());
                pst.setString(6, degree_combo.getSelectedItem().toString());
                pst.setString(7, job_txt.getText());
                pst.execute();
                clean();
                JOptionPane.showMessageDialog(null, "تم التسجيل بنجاح");
            } else {
                JOptionPane.showMessageDialog(null, "من فضلك املأ جميع البيانات!");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_add_btnActionPerformed

